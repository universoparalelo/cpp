/*
	Estudiaremso los conceptos de:
	.Clase
	.Objeto
	.Abstraccion
	.Encapsulacion
	.Herencia
	.Polimorfismo
	
	Estas son las ideas mas basicas que todo aquel que trabaje en POO debe comprender
	y manejar constantemente

	CLASE: una clase, es simplemente unas abstraccion que hacemos de nuestra experiencia
sensible. El ser humano tiende a agrupar seres o cosas -objetos- con caracteristicas similares
en grupos -clases-.

	OBJETO: un objeto es un conjunto de atributos y metodos, un objeto se deriva de
una clase. Atributo(caracteristicas). Metodos(acciones).

	ABSTRACCION: proceso mental de extraccion de las caracteristicas esenciales de algo,
ignorando los detalles superfluos. 

	ENCAPSULACION: proceso por el que se ocultan los detalles del soporte de las 
caracteristicas de una abstraccion. Como una pildora. 

	HERENCIA: la herencia es donde una clase nuevo se crea a partir de una clase ya existente.
Heredando todos sus atributos y metodos. Super clase o Clase parde y a la que hereda
Sub clase o Clase hija.

	POLIMORFISMO: por polimorfismo entendemos aquella cualidad que poseen los objetos para 
responder de distinto modo ante el mismo mensaje.
*/
